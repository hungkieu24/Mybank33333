    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        request.getRequestDispatcher("add-news.jsp").forward(request, response);
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        UserDAO udao = new UserDAO();
        NewsDAO ndao = new NewsDAO();
        
        String UserID_raw = request.getParameter("UserID");
        int UserID=0;
        try {
            UserID = Integer.parseInt(UserID_raw);
        } catch (NumberFormatException e) {
            System.out.println(e);
        }
        String Title = request.getParameter("Title");
        String Description = request.getParameter("Description");
        String Image = request.getParameter("Image");
        News newsToAdd = new News(0, udao.selectAnUserByConditions(UserID, "", "", ""), Title, Description, Image, true, null, 0);
        ndao.addANews(newsToAdd);
        response.sendRedirect("/timibank/seller/news-management?fromAdd=true");
    }

